/**
 * 测试授权规则
 *
 * @author zt
 * @date 2024/3/9
 **/
@Component
public class MyRequestOriginParser implements RequestOriginParser {
    @Override
    public String parseOrigin(HttpServletRequest httpServletRequest) {
        return httpServletRequest.getParameter("serverName");
    }
}
